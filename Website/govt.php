		    <p>Basically, the incentive is provided in the form of subsidies to manufactures of electric vehicles and infrastrcuture providers of electric vehicles.<br>

FAME was launched by the Ministry of Heavy Industries and Public Enterprises in 2015 to incentivize the production and promotion of eco-friendly vehicles including electric vehicles and hybrid vehicles.<br>

The Scheme operates in two phases.<br>

Phase I: started in 2015 and was completed on March 31st, 2019.<br>

Phase II: started from April 1st, 2019, will be completed by March 31st, 2022.<br>

Features of FAME<br>

FAME India is a part of the National Electric Mobility Mission Plan. Main thrust of FAME is to encourage electric vehicles by providing subsidies.  Under the NEMMP scheme, the government aimed to invest Rs 14000 crore in creating infrastructure and promoting the use of electric vehicles.<br>

Vehicles in most segments – two wheelers, three wheelers, electric and hybrid cars and electric buses obtained the subsidy benefit of the scheme.<br>

Focus areas of FAME<br>

The scheme covers Electric and Hybrid technologies like Mild Hybrid, Strong Hybrid, Plug in Hybrid & Battery Electric Vehicles.

FAME focuses on 4 areas i.e. Technology development, Demand Creation, Pilot Projects and Charging Infrastructure.

Benefits under FAME scheme<br>

Benefits of the FAME scheme was restructured after the launch of FAME -II. Incentives to the various type of vehicles are shown in a later part.<br>

FAME II<br>

The second phase is an expanded version of the first phase. FAME India Phase II has a total outlay of Rs 10000 Crores over the period of three years from 1st April 2019 to 2022.

Allocation for the scheme for 2019-20 is estimated to be at Rs 1,500-crore; Rs 5,000 crore in 2020-21 and Rs 3,500 crore in 2021-22.

FAME-II will cover buses using EV technology; electric, plug-in hybrid and strong hybrid four wheelers; electric three-wheelers including e-rickshaws and electric two-wheelers.

For the overall monitoring, sanctioning and implementation of the scheme, an inter-ministerial empowered committee – ‘Project Implementation and Sanctioning Committee’ (PISC) that is headed by the heavy industry secretary, shall be constituted.

Following are the incentives given to various categories of vehicles under FAME – II.  Incentives will be given on the basis of prescribed ex-factory price limits.

Electric two wheelers: incentives will be provided for 10 lakh registered electric two-wheelers of Rs 20,000 each.

e-rickshaws: incentives to 5 lakh e-rickshaws of Rs 50,000 each.

Electric four wheelers:  An incentive of Rs 1.5 lakh each to 35,000 electric four-wheelers with an ex-factory price of up to Rs 15 lakh.

Hybrid four wheelers: an incentive of Rs 13,000 each to 20,000 strong hybrid four-wheelers with ex-factory price of up to Rs 15 lakh.

e-buses: support to 7,090 e-buses with an incentive of up to Rs 50 lakh each having an ex-factory price of up to Rs 2 crore.

Electric charging infrastructure

The scheme proposes for establishing charging infrastructure and for this about 2700 charging stations will be established in metros, other million plus cities, smart cities and cities of Hilly states across the country. Objective is to create at least one charging station in a grid of 3 km x 3 km.

Similarly, establishment of charging stations are also proposed on major highways connecting major city clusters.

On the highways, charging stations will be established on both sides of the road at an interval of about 25 km each.

Of the total allocation, Rs 1,000 crore has been allocated for setting up charging stations for electric vehicles in India.</p>
